package org.gbros.utils.excel.object;

public class Column {

  private String name;
  private String type;

  public Column() {
  }

  public Column(String name, String type) {
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }

}
